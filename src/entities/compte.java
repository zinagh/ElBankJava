package entities;

/**
 *
 * @author MSI
 */
public class compte {
    private int id;

    public compte() {
    }

    public compte(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "compte{" + "id=" + id + '}';
    }
    
    
}
